function countChange(money, coins) {
  // dp[i] — количество способов составить сумму i из доступных монет.
  // Размер массива на 1 больше, чтобы включить сумму = money (так как индексация начинается с 0).
  const dp = Array(money + 1).fill(0);

  // Базовый случай: сумму 0 можно составить только одним способом — не использовать ни одной монеты.
  dp[0] = 1;

  // Перебираем каждую монету по очереди
  for (const coin of coins) {
    // Для каждой суммы от номинала монеты до искомой суммы
    for (let i = coin; i <= money; i++) {
      // Добавляем количество способов составить сумму (i - coin),
      // ведь к этим способам мы можем добавить текущую монету coin и получить сумму i.
      dp[i] += dp[i - coin];
      console.log(`dp[${i}] = ${dp[i]}`);
    }
  }

  // Возвращаем количество способов составить сумму money
  return dp[money];
}

console.log("итог:", countChange(4, [1, 2]));
